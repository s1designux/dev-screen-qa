"""
검수 오류 상태값의 '단일 진실 공급원(single source of truth)'.

CLAUDE.md 6번의 9개 상태값을 여기 한 곳에서만 정의한다.
'미해결'의 정의도 여기서만 바꾼다 (DoD③ 조회가 이 상수만 참조).
"""

# --- 9개 상태값 (CLAUDE.md 6번 그대로) ---
STATUS_ALL = (
    "발견",        # 1차/재검수에서 새로 찾음
    "개발확인",    # 개발자가 확인함
    "수정예정",    # 고치기로 함
    "수정완료",    # 개발이 고쳤다고 보고 (아직 검증 전)
    "재검수필요",  # 다시 봐야 함
    "검수완료",    # 디자이너가 확인, 종결
    "보류",        # 의도적으로 미룸
    "오류아님",    # 오류 아님으로 판정, 종결
    "재발",        # 고쳤다가 다시 어긋남
)

# --- 미해결(open) 집합: "아직 조치가 필요한 살아있는 오류" ---
# 이 튜플이 미해결의 '정의'다. DoD③("현재 미해결 오류만 추출")는
# 반드시 이 상수를 참조한다. 여기만 바꾸면 정의 전체가 바뀐다.
#
# 경계값 결정 근거:
#   - 수정완료 : 개발이 고쳤다고 '보고'만 한 상태. 아직 디자이너 검증 전이지만
#                '깨진 상태'는 아니므로 미해결에서 제외. (검증은 재검수필요/검수완료로 이동)
#                → 만약 "검증 전은 전부 미해결로 본다"면 아래 튜플에 "수정완료"를 추가.
#                ※ 재검토 예정: 수정완료 포함 여부는 실데이터 확인 후 결정.
#   - 검수완료 / 오류아님 : 종결 → 제외
#   - 보류 : 의도적 미룸 → 현재 조치 목록에서 제외
UNRESOLVED_STATUSES = (
    "발견",
    "개발확인",
    "수정예정",
    "재검수필요",
    "재발",
)

# 종결(해결·닫힘) 집합 — 참고용 (미해결의 여집합 중 종결로 간주하는 것)
CLOSED_STATUSES = ("검수완료", "오류아님")


def is_unresolved(status: str) -> bool:
    """주어진 상태가 미해결 집합에 속하는지."""
    return status in UNRESOLVED_STATUSES
